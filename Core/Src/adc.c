float my_adc_read()
{
	static uint32_t Value[30];
	static uint32_t AD_Value,max,min;
	static float AD_tr=0;
	
	for(char n=0;n<22;n++) 
			{//取22个值做滤波用
					HAL_ADC_Start(&hadc1);
					HAL_ADC_PollForConversion(&hadc1, 10);    //等待转换完成，第二个参数表示超时时间，单位ms        
					if(HAL_IS_BIT_SET(HAL_ADC_GetState(&hadc1), HAL_ADC_STATE_REG_EOC))
					{
							Value[n]=HAL_ADC_GetValue(&hadc1);
							AD_Value += Value[n];
					}                
			}
			max=Value[0];
			min=Value[0];
			for(char n=0;n<22;n++)//取最大值、最小值
			{
					max=(Value[n]<max)?max:Value[n];    
					min=(min<Value[n])?min:Value[n];
			}        
			AD_tr=(float)((AD_Value -max-min)/20)*(3.1/4096);    //这里我做了个去掉最大最小值后，取均值的软件滤波   
			AD_Value=0;
			return AD_tr;
}
